#include <iostream>
using namespace std;

int main() {
    cout << "Size of int: " << sizeof(int) << " bytes" << endl; //print int value in byte
    cout << "Size of float: " << sizeof(float) << " bytes" << endl; //print float value in byte
    cout << "Size of double: " << sizeof(double) << " bytes" << endl; //print double value in byte
    cout << "Size of char: " << sizeof(char) << " byte" << endl; //print char  value in byte
    
    return 0;
}