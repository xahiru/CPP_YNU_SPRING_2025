// CPP Program to demonstrate the increment
// and decrement operators
#include <iostream>
using namespace std;

int main()
{
	int a = 10;
	cout << "a++ is " << a++ << endl;
	cout << "++a is " << ++a << endl;

	int b = 15;
	cout << "b-- is " << b-- << endl;
	cout << "--b is " << --b << endl;
	
	return 0;
}