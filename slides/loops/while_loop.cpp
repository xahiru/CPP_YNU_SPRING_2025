#include <iostream>
using namespace std;

int main() {
  
      // Initialization
      int i = 1;
  
      // while loop that starts with i = 1 and ends
      // when i is greater than 5.
    while (i <= 5) {
        cout << i << " ";
      
          // Updation
          i++;
    }

    return 0;
}
